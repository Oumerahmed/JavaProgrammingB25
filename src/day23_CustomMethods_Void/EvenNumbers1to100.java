package day23_CustomMethods_Void;

public class EvenNumbers1to100 {

    public static void main(String[] args) {

        EvenNumbers1to100();

    }





    public static void EvenNumbers1to100(){

        for (int i = 0; i <101; i++){

            if (i<101 && i%2==0){

                System.out.print(i+"  ");
            }
        }

    }





}
